   private int installsaleID;
   private String categoryID;
   private int productID;
   private int dealsend;
   private int send;
   private int install;
   private int sendInstall;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getInstallsaleID() {
	return installsaleID;
}
public void setInstallsaleID(int installsaleID) {
	this.installsaleID = installsaleID;
}
 
public String getCategoryID() {
	return categoryID;
}
public void setCategoryID(String categoryID) {
	this.categoryID = categoryID;
}
public int getProductID() {
	return productID;
}
public void setProductID(int productID) {
	this.productID = productID;
}
public int getDealsend() {
	return dealsend;
}
public void setDealsend(int dealsend) {
	this.dealsend = dealsend;
}
public int getSend() {
	return send;
}
