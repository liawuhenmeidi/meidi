package branchtype;

public class BranchType {
   private int id ;
   private String name ;
   private int statues; 
   
public int getStatues() {
	return statues;
}
public void setStatues(int statues) {
	this.statues = statues;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
}
