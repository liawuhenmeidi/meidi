package message;

public class Message {
   private int oid ;
   private String message ;
   
   
public int getOid() {
	return oid;
}
public void setOid(int oid) {
	this.oid = oid;
}
public String getMessage() {
	return message;
}
public void setMessage(String message) {
	this.message = message;
}
}
